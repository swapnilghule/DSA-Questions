class Solution{
    public:
    // Function to find number of bits needed to be flipped to convert A to B
    int countBitsFlip(int a, int b){
        
        // Your logic here
        int count=0;
        
        while(a>0 || b>0)
        {
            int a_LSB= a&1;
            int b_LSB= b&1;
            
            if(a_LSB != b_LSB)
            count++;
            
            a>>=1;
            b>>=1;
        }
        
        
        return count;
        
    }
};

